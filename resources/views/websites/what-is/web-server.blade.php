            <h2>What is a Web Server?</h2>

            <p>Essentially, a web server is just a computer. It doesn't have a mouse, keyboard or a screen connected to it. It never turns off, and it is sitting in an air conditioned warehouse in a stack with many many similar computers.</p>

            <img class="m-x-auto max-height-300 m-b-30 img img-responsive" src="/svg/undraw_server_cluster.svg" alt="a server stack connected to devices">

            <p>Web servers are the computers that are <b>responsible for executing back end source code and returning the requested page</b> to the web browser. They may also have databases set up on them to store data. Just like your own computer, they can be upgraded with extra memory or storage depending on the amount of traffic a website gets.</p>

            <p>When you visit a website, your web browser is actually making a request to a web server that contains the source code for the website. Unlike your own computer, the web server is configured to allow certain files (mainly the source code of the website) to be accessed by anyone from any computer in the world.</p>

            <p>These days, is unlikely that your web developer actually has a web server sitting in their office. There are many reliable services that will rent out the use of web servers for a monthly fee, including Amazon, Digital Ocean and Vultr. These companies work hard to make sure their servers are stable, load quickly, and are available all the time from any location.</p>
