            <h2>What is a Domain Name?</h2>

            <p>Each web server has it's own IP address. This is a number that looks something like "127.0.0.1" and is the main way the server can be accessed. Obviously, nobody is going to be handing out business cards with the IP address of their web server on them, so thats where domain names come in handy!</p>

            <p>A domain name is the <b>name we associate with a website</b> rather than it's IP address. For example johnclendvoy.ca, google.com, or wikipedia.org are all domain names.</p>

            <img class="m-x-auto max-height-300 m-b-30 img img-responsive" src="/svg/undraw_domain_names.svg" alt="choosing a domain name">

            <p>A domain name is easy to register for. There are many sites that offer this service, such as NameCheap and GoDaddy to name a few. most domain names cost relitively little to register for a year, but some, depending on their length, or subject, can cost quite a bit.</p>

            <p>Once your domain is registered to you, you will be given permission to attach that domain name to a web server of your choice. If you already have the source code of your website written, and it is available on a web server, you can associate your domain name with that web server, that way people can be directed to your site by entering your domain name!</p>
